// @lc code=start
class Solution {
public:
    int flood(vector<vector<int>>& grid,int x, int y){
        int res = 0;
        if(x < 0 || x >= grid.size() || y < 0 || y >= grid[0].size()){
            return res;
        }
        if(grid[x][y] == 0) return res;
        grid[x][y] = 0;
        res++;
        res += flood(grid,x+1,y);
        res += flood(grid,x-1,y);
        res += flood(grid,x,y+1);
        res += flood(grid,x,y-1);
        return res;

    }
    int maxAreaOfIsland(vector<vector<int>>& grid) {
        int res = 0;
        for(int x = 0; x < grid.size(); x++){
            for(int y = 0; y < grid[0].size(); y++){
                if(grid[x][y] == 1){
                    res = max(res, flood(grid,x,y));
                }
            }
        }
        return res;
    }
};
