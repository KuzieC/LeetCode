// @lc code=start
class Solution {
public:
    int lengthOfLongestSubstring(string s) {
        int left = 0, right = 0,curr = 0;
        int siz = s.size();
        unordered_map<char,int> mp;
        int res = 0;
        while(right < siz){
            if(mp[s[right]] < 1){
                mp[s[right]]++;
                right++;
                curr++;
            }
            else{
                while(mp[s[right]] > 0){
                    mp[s[left]]--;
                    left++;
                    curr--;
                }
            }
            res = max(res,curr);
        }
        return res;
    }
};
